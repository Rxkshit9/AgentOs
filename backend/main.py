@app.get("/threads")
def list_threads():
    """Lists all unique conversation thread IDs from the checkpoints table."""
    try:
        with SessionLocal() as session:
            from sqlalchemy import text
            result = session.execute(text("SELECT DISTINCT thread_id FROM checkpoints ORDER BY thread_id"))
            threads = [row[0] for row in result]
        return {"threads": threads}
    except Exception as e:
        logger.error(f"Error listing threads: {e}")
        return {"threads": []}

@app.get("/threads/{thread_id}/messages")
def get_thread_messages(thread_id: str):
    """Retrieves the conversation history for a given thread ID."""
    try:
        config = {"configurable": {"thread_id": thread_id}}
        state = app.state.graph.get_state(config)
        messages = state.values.get("messages", [])
        
        serializable = []
        for msg in messages:
            role = "user" if msg.type == "human" else "assistant"
            serializable.append({"role": role, "content": msg.content})
        return {"messages": serializable}
    except Exception as e:
        logger.error(f"Error fetching thread messages: {e}")
        return {"messages": []}
