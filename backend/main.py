@app.post("/chat")
def chat_endpoint(payload: ChatPayload):
    """Executes the agent workflow synchronously and returns the final response."""
    # Set active model if specified
    if payload.model_name:
        settings.DEFAULT_MODEL = payload.model_name
        
    # Set active project workspace target directory
    settings.ACTIVE_PROJECT_DIR = payload.project_dir
        
    config = {"configurable": {"thread_id": payload.thread_id}}
    initial_state = {
        "messages": [{"type": "human", "content": payload.message}],
        "agent_logs": []
    }
    
    try:
        final_state = app.state.graph.invoke(initial_state, config=config)
        messages = final_state.get("messages", [])
        
        # Look for the last response generated by reflection or any AI agent
        ai_messages = [msg for msg in messages if msg.type == "ai"]
        final_reply = ai_messages[-1].content if ai_messages else "No final response generated."
        
        return {
            "response": final_reply,
            "logs": final_state.get("agent_logs", []),
            "confidence_score": final_state.get("confidence_score", 100),
            "plan": final_state.get("plan", {})
        }
    except Exception as e:
        logger.error(f"Error in chat endpoint: {e}")
        raise HTTPException(status_code=500, detail=str(e))

@app.post("/stream")
def stream_endpoint(payload: ChatPayload):
    """Streams step-by-step agent executions and findings using Server-Sent Events (SSE)."""
    if payload.model_name:
        settings.DEFAULT_MODEL = payload.model_name
        
    # Set active project workspace target directory
    settings.ACTIVE_PROJECT_DIR = payload.project_dir
        
    config = {"configurable": {"thread_id": payload.thread_id}}
    initial_state = {
        "messages": [{"type": "human", "content": payload.message}],
        "agent_logs": []
    }
    
    def event_generator():
        try:
            # We stream graph executions
            # Each step returns updates to the state
            for chunk in app.state.graph.stream(initial_state, config=config, stream_mode="updates"):
                # A chunk looks like: { "node_name": { "messages": [...], "agent_logs": [...] } }
                for node_name, updates in chunk.items():
                    # Check if there are new logs in this update
                    logs = updates.get("agent_logs", [])
                    # Send node status updates
                    log_data = {
                        "type": "log",
                        "node": node_name,
                        "logs": logs
                    }
                    yield f"data: {json.dumps(log_data)}\n\n"
                    
            # Once stream is complete, retrieve the final state to return the synthesized message
            state = app.state.graph.get_state(config)
            messages = state.values.get("messages", [])
            ai_messages = [msg for msg in messages if msg.type == "ai"]
            final_reply = ai_messages[-1].content if ai_messages else "No response generated."
            
            final_data = {
                "type": "final",
                "response": final_reply,
                "confidence_score": state.values.get("confidence_score", 100),
                "plan": state.values.get("plan", {})
            }
            yield f"data: {json.dumps(final_data)}\n\n"
            
        except Exception as e:
            logger.error(f"Error in streaming: {e}")
            err_data = {
                "type": "error",
                "detail": str(e)
            }
            yield f"data: {json.dumps(err_data)}\n\n"
            
    return StreamingResponse(event_generator(), media_type="text/event-stream")

@app.get("/memory")
def get_memories():
    """Retrieves all long-term user memories stored in PostgreSQL."""
    try:
        store = PostgresStore()
        items = store.search(namespace_prefix=("default_user", "memories"))
        return {
            "memories": [
                {"key": item.key, "value": item.value.get("content", ""), "updated_at": item.updated_at}
                for item in items
            ]
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

@app.post("/memory")
def add_memory(payload: MemoryPayload):
    """Manually adds or updates a long-term user memory record."""
    try:
        store = PostgresStore()
        store.put(
            namespace=("default_user", "memories"),
            key=payload.key,
            value={"content": payload.value}
        )
        return {"status": "success", "message": f"Memory '{payload.key}' stored."}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

