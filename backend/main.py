saver_context = None

@asynccontextmanager
async def lifespan(app: FastAPI):
    global saver_context
    use_postgres = os.getenv("USE_POSTGRES_CHECKPOINTER", "false").lower() in {"1", "true", "yes", "on"}

    if use_postgres and PostgresSaver is not None:
        try:
            conn_str = DATABASE_URL.replace("+psycopg", "")
            saver_context = PostgresSaver.from_conn_string(conn_str)
            checkpointer = saver_context.__enter__()
            app.state.graph = compile_graph(checkpointer)
            logger.info("LangGraph compiled successfully with PostgresSaver.")
            yield
        except Exception as e:
            logger.warning(f"Falling back to in-memory checkpointer: {e}")
            checkpointer = MemorySaver()
            app.state.graph = compile_graph(checkpointer)
            logger.info("LangGraph compiled successfully with MemorySaver fallback.")
            yield
        finally:
            if saver_context:
                saver_context.__exit__(None, None, None)
                logger.info("PostgresSaver connection pool closed.")
    else:
        checkpointer = MemorySaver()
        app.state.graph = compile_graph(checkpointer)
        logger.info("LangGraph compiled successfully with MemorySaver (default).")
        yield

